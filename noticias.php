<h2>Windows 8 e seus rumores</h2>
 
<p>Depois do Windows 7, o Windows 8 já traz seus rumores. Ultimamente a Microsoft está colocando diversos serviços de integração, como o Windows Live Messenger 2010 que já integra Facebook e MySpace como padrão, e outros diversos serviços. Entre seus principais rumores estão:</p>
<br />

<p>CircleDock: já era rumor do Seven, mas não foi aderido, mas no Windows 8 deve contar com muitas funcionalidades a mais, como por ex. o icone do centro pode se transformar em um player, ou uma tela para acompanhar o Windows. E é claro ainda contando com os atalhos ao seu redor.</p><br />

<p>Logon: será aprimorado, além do teclado virtual para Tablets, também deve contar com reconhecimento facial, e aprimoramento da leitura biométrica (digitais).</p>
<br />

Controlar:  o Windows 8 poderá ser usado por movimentos ou por voz.
<br />

<p>Integração: seus serviços mais integrados, ou aliados a outros serviços, quem sabe se o Wordpad enviar diretamente posts para o Blogger, WordPress ou MySpace?</p>
<br />

<p>Media Center: na minha opinião um dos aplicativos melhores produzidos pela Microsoft, pode ser o próprio Windows, algum acham que ele será a base de tudo, assim como o Chrome OS tem sua base num navegador, o Windows 8 terá base numa central de midia.</p>
<br />

<p>Senhas: arquivos e pastas terão seu acesso através de uma senha mestre, se aplicado pelo administrador, colocando senha com facilidade aos seus arquivos.
<br />

<p>Barra: a barra normal do Windows sumirá, o botão do Windows poderá ser centralizado abrindo o CircleDock, ou os itens abertos se transformarão em pequenos botões na parte inferior da tela, sem aquela barra que os enquadra.</p>
<br />

<p>Na nuvem: você poderá salvar arquivos na Internet diretamente, não precisando mais de backup, com acesso em qualquer lugar.</p>
<br />

<p>Vidro: tema do Windows normal, mas melhorada com efeitos, como quando a janela está inativa terá um efeito de vidro e ficará mais transparente.</p>
<br />

<p>3D: essa tecnologia pode ser aliada ao Windows 8, mas poderá exigir placas gráficas 3D, ou então pode apenas contar com aplicativos em 3D, como mostrar diversas áreas de trabalhos como um cubo.</p>
<br />

Vale lembrar que são apenas rumores, não confirmados pela Microsoft.
<br />

