<h1>Novidades</h1>
<h2>YouTube aumenta dura&ccedil;&atilde;o de vídeos para 15 minutos</h2>
<br />
<b>Medida só não foi implantada antes porque ferramenta que monitora direitos autorais ainda não estava bem desenvolvida.</b>

<p>Cinco minutos fazem diferença para você? Para o YouTube faz. O blog oficial do canal de vídeos acaba de anunciar que ampliará o tempo limite dos materiais enviados pelos usuários de 10 para 15 minutos.</p> 

<p>De acordo com o Google, a medida não foi implantada antes por conta dos problemas que o canal já havia enfrentado com vídeos sem autorização e o limite de tempo ajudava a monitorar com mais facilidade a questão dos direitos autorais. A equipe do YouTube disse ainda que o Content ID e outras ferramentas destinadas a prevenir a pirataria no site sofreram diversos avanços tecnológicos.</p>

<p>E para comemorar os minutinhos a mais, o YouTube lançou um concurso para que os usuários tenham a oportunidade de ter seus vídeos destacados na home. Para isso, a equipe pede que os usuários adicionem a tag "yt15minutes" às postagens realizadas até a próxima quarta-feira, dia 04/08.</p>

